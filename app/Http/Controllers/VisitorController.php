    {
        $visitor_model = new Visitor ;

        $ip = Request()->ip() ;

        if ($visitor_model->check_ip($ip)) {
            $result = $visitor_model->create_column($ip) ; 
            return response()->json([ 'result' => $result ]) ;
        }else {
            return response()->json([ 'result' => false ]) ;
        }
    }

